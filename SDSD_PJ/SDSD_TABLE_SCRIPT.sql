CREATE TABLE MEMBER (
    MEM_NUMBER NUMBER(10),
    MEM_NAME VARCHAR2(20) NOT NULL,
    MEM_NICKNAME VARCHAR2(20)NOT NULL,
    MEM_EMAIL VARCHAR2(30)UNIQUE,
    MEM_PWD VARCHAR2(100) NOT NULL,
    MEM_PHONE VARCHAR2(20) NOT NULL,
    MEM_GENDER VARCHAR2(10) NOT NULL,
    MEM_BDAY VARCHAR2(20) DEFAULT '20000101',
    MEM_ADDRESS VARCHAR2(30) NOT NULL,
    MEM_INTRODUCE VARCHAR2(500) DEFAULT '반갑습니다',
    MEM_STATUS VARCHAR2(10) DEFAULT 'Y'
);

ALTER TABLE MEMBER ADD CONSTRAINT MEM_NUMBER_PK PRIMARY KEY (MEM_NUMBER);

COMMENT ON COLUMN MEMBER.MEM_NUMBER IS '회원번호';
COMMENT ON COLUMN MEMBER.MEM_NAME IS '회원이름';
COMMENT ON COLUMN MEMBER.MEM_NICKNAME IS '닉네임';
COMMENT ON COLUMN MEMBER.MEM_EMAIL IS '이메일';
COMMENT ON COLUMN MEMBER.MEM_PWD IS '비밀번호';
COMMENT ON COLUMN MEMBER.MEM_PHONE IS '핸드폰번호';
COMMENT ON COLUMN MEMBER.MEM_GENDER IS '성별';
COMMENT ON COLUMN MEMBER.MEM_BDAY IS '생년월일';
COMMENT ON COLUMN MEMBER.MEM_ADDRESS IS '활동지역';
COMMENT ON COLUMN MEMBER.MEM_INTRODUCE IS '회원소개';
COMMENT ON COLUMN MEMBER.MEM_STATUS IS '상태';

