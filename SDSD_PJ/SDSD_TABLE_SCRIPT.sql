CREATE TABLE BOARD (
    BOR_ID NUMBER(10),
    BOR_WRITER_NO NUMBER,
    BOR_WRITER_NAME VARCHAR2(20) NOT NULL,
    BOR_TITLE VARCHAR2(40) NOT NULL,
    BOR_CONTENT VARCHAR2(2000) NOT NULL,
    BOR_NUMBER NUMBER NOT NULL,
    CREATE_DATE DATE DEFAULT SYSDATE,
    CORRECT_DATE DATE DEFAULT SYSDATE,
    READCOUNT NUMBER DEFAULT 0,
    BOR_STATUS VARCHAR2(5) DEFAULT 'Y',
    ORIGINAL_FILE VARCHAR2(100),
    RENAMED_FILE VARCHAR2(100)
);

ALTER TABLE BOARD ADD CONSTRAINT BOR_ID_PK PRIMARY KEY(BOR_ID);
ALTER TABLE BOARD ADD CONSTRAINT BOR_WRITER_NO_FK FOREIGN KEY(BOR_WRITER_NO) REFERENCES MEMBER(MEM_NUMBER);

COMMENT ON COLUMN BOARD.BOR_ID IS '게시판ID';
COMMENT ON COLUMN BOARD.BOR_WRITER_NO IS '작성자 넘버';
COMMENT ON COLUMN BOARD.BOR_WRITER_NAME IS '작성자 이름';
COMMENT ON COLUMN BOARD.BOR_TITLE IS '게시글제목';
COMMENT ON COLUMN BOARD.BOR_CONTENT IS '게시글내용';
COMMENT ON COLUMN BOARD.BOR_NUMBER IS '게시글 번호';
COMMENT ON COLUMN BOARD.CREATE_DATE IS '작성날짜';
COMMENT ON COLUMN BOARD.CORRECT_DATE IS '수정날짜';
COMMENT ON COLUMN BOARD.READCOUNT IS '조회수';
COMMENT ON COLUMN BOARD.BOR_STATUS IS '상태';
COMMENT ON COLUMN BOARD.ORIGINAL_FILE IS '첨부파일원래이름';
COMMENT ON COLUMN BOARD.RENAMED_FILE IS '첨부파일변경이름';


COMMIT;